public class FloydsTriangle 
{
    public static void main(String args[])
    {
        for(int i=1;i<=10;i++)
        {
            if((i!=1)&&(i!=3)&&(i!=6))
            {
                System.out.print(i +" ");
            }
            else
            {
                System.out.println(i +" ");
            }
        }
    }
}